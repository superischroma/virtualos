const uint32_t VGA_COLORS[] = {
	0x000000, 0x0000AA, 0x00AA00, 0x00AAAA, 0xAA0000, 0xAA00AA, 0xAA5500, 0xAAAAAA, 0x555555, 0x5555FF, 0x55FF55, 0x55FFFF, 0xFF5555, 0xFF55FF, 0xFFFF55, 0xFFFFFF
};

SDL_Window* window = NULL;

void finalize_vga(void)
{
    SDL_DestroyWindow(window);
    SDL_Quit();
}

bool init_vga(void)
{
    if (SDL_Init(SDL_INIT_VIDEO) < 0)
    {
        errorf("Failed to initialize SDL\n");
        return false;
    }

    window = SDL_CreateWindow("virtualos", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, SCREEN_X, SCREEN_Y, 0);

    if (!window)
    {
        errorf("Failed to create window\n");
        return false;
    }

    return true;
}

bool work_vga(void)
{
    SDL_Event e;
    while (SDL_PollEvent(&e))
    {
        if (e.type == SDL_QUIT)
            return false;
    }
    SDL_UpdateWindowSurface(window);
    return true;
}

void put_vga_glyph(vos_t* vos, uint32_t x, uint32_t y, char glyph, uint8_t foreground, uint8_t background)
{
	for (uint32_t i = 0; i < 16; ++i)
	{
		uint8_t bits = VGA_GLYPHS[glyph * 16 + i];
		for (uint32_t j = 0; j < 8; ++j)
		{
			bool on = (bits >> (7 - j)) & 1;
			SDL_Rect rect;
			rect.x = (x * 8 + j) * SCREEN_UPSCALE;
			rect.y = (y * 16 + i) * SCREEN_UPSCALE;
			rect.h = rect.w = SCREEN_UPSCALE;
			SDL_FillRect(SDL_GetWindowSurface(window), &rect, on ? VGA_COLORS[foreground] : VGA_COLORS[background]);
		}
	}
	*((uint16_t*) (vos->mem + vos->vga_addr + x + (y * VGA_BUFFER_GLYPH_WIDTH))) = ((uint16_t) glyph) | (((uint16_t) (foreground & 0x0F)) << 8) | (((uint16_t) (background & 0x0F)) << 12);
}